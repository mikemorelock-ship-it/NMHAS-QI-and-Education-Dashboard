// seed-standardized-metrics.mjs — Additive script to insert standardized metrics
// Safe to re-run: skips metrics whose slug already exists in the target department.
// Does NOT clear or modify existing data.
//
// Usage:
//   npx tsx scripts/seed-standardized-metrics.mjs
//   DATABASE_URL="libsql://..." TURSO_AUTH_TOKEN="..." npx tsx scripts/seed-standardized-metrics.mjs

import { fileURLToPath } from "node:url";
import path from "node:path";
import { readFileSync } from "node:fs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");

const { PrismaClient } = await import("../src/generated/prisma/client.ts");

// ---------------------------------------------------------------------------
// Database connection (same pattern as seed.mjs — supports local + Turso)
// ---------------------------------------------------------------------------
let dbUrl;
try {
  const envContent = readFileSync(path.join(projectRoot, ".env"), "utf-8");
  const urlMatch = envContent.match(/^DATABASE_URL="?([^"\r\n]+)"?/m);
  dbUrl = urlMatch ? urlMatch[1] : `file:${path.join(projectRoot, "dev.db")}`;
} catch {
  dbUrl = `file:${path.join(projectRoot, "dev.db")}`;
}
dbUrl = process.env.DATABASE_URL || dbUrl;
console.log("Using database:", dbUrl);

let adapter;
if (dbUrl.startsWith("libsql://") || dbUrl.startsWith("https://")) {
  const { PrismaLibSql } = await import("@prisma/adapter-libsql");
  adapter = new PrismaLibSql({
    url: dbUrl,
    authToken: process.env.TURSO_AUTH_TOKEN,
  });
} else {
  const { PrismaBetterSqlite3 } = await import("@prisma/adapter-better-sqlite3");
  adapter = new PrismaBetterSqlite3({ url: dbUrl });
}
const prisma = new PrismaClient({ adapter });

// ---------------------------------------------------------------------------
// Lookup existing department + categories by name
// ---------------------------------------------------------------------------
