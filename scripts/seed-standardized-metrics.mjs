let created = 0;
let skipped = 0;

for (const m of metrics) {
  // Check if slug already exists in this department
  const existing = await prisma.metricDefinition.findUnique({
    where: { departmentId_slug: { departmentId: deptId, slug: m.slug } },
  });

  if (existing) {
    console.log(`  SKIP: "${m.name}" (slug "${m.slug}" already exists)`);
    skipped++;
    continue;
  }

  const categoryId = m.cat === "safety" ? safetyId : clinicalId;

  await prisma.metricDefinition.create({
    data: {
      name: m.name,
      slug: m.slug,
      departmentId: deptId,
      categoryId,
      categoryLegacy: m.categoryLegacy,
      description: m.description,
      dataDefinition: m.dataDefinition,
      unit: m.unit,
      chartType: m.chartType,
      periodType: m.periodType,
      isKpi: m.isKpi,
      target: m.target ?? null,
      aggregationType: m.aggregationType,
      dataType: m.dataType,
      desiredDirection: m.desiredDirection,
      source: m.source,
      numeratorLabel: m.numeratorLabel ?? null,
      denominatorLabel: m.denominatorLabel ?? null,
      rateMultiplier: m.rateMultiplier ?? null,
      rateSuffix: m.rateSuffix ?? null,
      sortOrder: m.sortOrder,
      isActive: true,
    },
  });

  console.log(`  ADD:  "${m.name}" [${m.source}]`);
  created++;
}

